package javabasic;

public class DataType {
	public static void main(String[] args) {
		
		//Primitive : Kiểu dữ liệu gốc
		/*
		 * Boolean : 		boolean
		 * Numeric : Character 
		 * 				 	char
		 * 		     Integral  
		 * 				 Interger
		 * 					byte
		 * 					short
		 * 					int
		 * 					long
		 * 				 Floating-point
		 * 					float
		 * 					double
		 * 
		 * Non-Primitive : Kiểu dữ liệu tham chiếu/đối tượng
		 * 
		 * 					String
		 *				    Array
		 * 					etc.
		 * 
		 * */
		int a = 10;
		double b = 20.3;
		double c = 3.14785;
		boolean x = true;
		x= false;
		System.out.println("a :" + a + "\nb :" + b + "\nc : " + c + "\nx : " + x);
	}
}
